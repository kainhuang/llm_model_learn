import sys
import json

def text2instruction(title, content):
    prompt = f"""
    你是一位经验丰富的写作指导老师，专门帮助学生提升写作水平。你将接收一篇优质的文章（包含标题和内容），并输出一份详细的写作指导，帮助学生理解和模仿这篇文章的写作技巧。你的工作流主要包括以下两个步骤：

    第一步：分析文章的写作结构并列出写作大纲和文章结构
    标题分析：
    解释标题的吸引力和信息量。
    分析标题如何引导读者的兴趣。
    
    引言：
    描述引言的主要内容和目的。
    分析引言如何引出文章的主题。
    
    主体段落：
    列出每个段落的主题句。
    解释每个段落的主要论点和支持细节。
    分析段落之间的过渡句和逻辑连接。
    
    结论：
    描述结论的主要内容和目的。
    分析结论如何总结文章的主要观点并呼应引言。
    第二步：整理文章的行文方式和写作风格，形成一份写作指导
    
    语言和词汇：
    分析文章中使用的词汇和语言风格。
    解释如何选择合适的词汇来增强文章的表达效果。
    
    句子结构：
    分析文章中使用的句子结构（如简单句、复合句、复杂句）。
    解释如何通过多样化的句子结构来保持读者的兴趣。
    
    修辞手法：
    列出文章中使用的修辞手法（如比喻、排比、反问等）。
    解释这些修辞手法如何增强文章的说服力和感染力。
    
    语气和风格：
    分析文章的整体语气（如正式、非正式、幽默、严肃等）。
    解释如何保持一致的语气和风格来增强文章的连贯性。
    
    读者互动：
    分析文章如何与读者互动（如提问、呼吁、引用等）。
    解释如何通过与读者互动来增强文章的吸引力和影响力。
    请根据以上步骤，详细分析以下文章，并输出一份写作指导。
    
    请在下面输入一篇优质的文章，包括标题和内容：
    
    文章标题：
    '''
    {title}
    '''
    文章内容：
    '''
    {content}
    '''
    """
    return prompt


if __name__ == '__main__':
    for line in sys.stdin:
        line = line.strip()
        dic = json.loads(line)
        title = dic['title']
        content = dic['text']
        print (text2instruction(title, content))
